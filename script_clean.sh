#!/bin/bash
# ↑ Указывает системе запускать скрипт через Bash (оболочку командной строки)

echo "🧹 Cleaning project..."
# ↑ Просто вывод сообщения в терминал (для удобства и логирования)

# ⚡ Очистка Hugo

rm -rf public
# ↑ Удаляет папку с готовым сайтом (HTML, CSS, JS)
# Зачем: пересобрать сайт полностью с нуля (чистый билд)

rm -rf resources
# ↑ Удаляет кеш Hugo (очень важно!)
# Внутри: оптимизированные изображения, fingerprint (хэши файлов для кеша браузера)
# сгенерированные CSS/JS, чтобы исправить баги с изображениями, стилями, SEO-хэшами

rm -rf .hugo_build.lock
# ↑ Удаляет lock-файл Hugo, иногда он "зависает" и блокирует сборку проекта

rm -rf hugo_stats.json
# ↑ Удаляет файл hugo_stats.json

# ⚡ Очистка Node / Tailwind

rm -rf node_modules
# ↑ Удаляет ВСЕ установленные npm-пакеты (Tailwind, PostCSS и другие зависимости)

rm -rf .cache
# ↑ Удаляет кеш сборщиков (например, PostCSS, Tailwind), иногда старые стили остаются из-за этого кеша

rm -rf package-lock.json
# ↑ Удаляет lock-файл зависимостей, он фиксирует версии пакетов
# Зачем удалять: если зависимости сломались, если хочешь пересобрать всё "с нуля"

# ⚡ Переустановка зависимостей

echo "📦 Reinstalling..."
# ↑ Сообщение о начале установки зависимостей

npm install
# ↑ Скачивает заново все зависимости из package.json
# После удаления node_modules это обязательный шаг

# ⚡ Завершение

echo "🚀 Done. Run dev servers."
# ↑ Финальное сообщение
# После этого нужно запустить: Tailwind (npm run dev), Hugo (hugo server)


