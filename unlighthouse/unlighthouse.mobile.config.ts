import { defineUnlighthouseConfig } from 'unlighthouse/config'
import { cleanPuppeteerOptions, qualityCategories } from './unlighthouse.shared.ts'

/**
 * Unlighthouse Mobile Config
 * ------------------------------------------------------------
 * Назначение файла:
 * Проверять сайт https://aerocool.ua в мобильном режиме.
 *
 * Почему mobile важен:
 * Google использует mobile-first indexing, поэтому мобильная версия
 * сайта особенно важна для SEO.
 *
 * Когда запускать:
 * - после изменения дизайна;
 * - после добавления новых изображений;
 * - после добавления Tailwind-блоков;
 * - после изменения шаблонов Hugo;
 * - после изменения SEO-разметки;
 * - после изменения страниц товаров.
 *
 * Команда запуска:
 * npm run audit:mobile
 *
 * Или напрямую:
 * npx unlighthouse --config-file unlighthouse.mobile.config.ts
 */

export default defineUnlighthouseConfig({
  /**
   * site
   * ------------------------------------------------------------
   * Основной production-домен сайта.
   *
   * Unlighthouse будет использовать этот адрес как базовый URL.
   * Он найдёт sitemap.xml, внутренние ссылки и будет запускать
   * Lighthouse-аудиты для найденных страниц.
   */
  site: 'https://aerocool.ua',

  /**
   * outputPath
   * ------------------------------------------------------------
   * Папка, куда Unlighthouse сохранит результаты этого аудита.
   *
   * Мы разделяем отчёты по типам:
   * - reports/mobile
   * - reports/desktop
   * - reports/production
   * - reports/strict
   * - reports/preview
   * - reports/critical-urls
   *
   * Так отчёты не смешиваются между собой.
   */
  outputPath: './reports/mobile',

  /**
   * cache
   * ------------------------------------------------------------
   * false = каждый запуск делает свежую проверку.
   *
   * Для аудита после изменений лучше cache: false,
   * потому что нам нужны реальные текущие данные, а не старый результат.
   */
  cache: false,

  puppeteerOptions: cleanPuppeteerOptions,

  /**
   * scanner
   * ------------------------------------------------------------
   * Здесь настраивается то, КАК Unlighthouse будет находить и проверять URL.
   */
  scanner: {
    /**
     * device
     * ------------------------------------------------------------
     * mobile = проверка как на мобильном устройстве.
     *
     * Это главный режим для SEO-аудита.
     */
    device: 'mobile',

    /**
     * throttle
     * ------------------------------------------------------------
     * true = использовать замедление сети/CPU для более реалистичного теста.
     *
     * Без throttling тест может быть слишком оптимистичным.
     */
    throttle: true,

    /**
     * samples
     * ------------------------------------------------------------
     * Количество прогонов на одну страницу.
     *
     * 1 = быстро, но результат может немного прыгать.
     * 2–3 = лучше для production-аудита.
     * 5 = очень строго, но долго.
     *
     * Для mobile ставим 3, потому что mobile-метрики часто более чувствительные.
     */
    samples: 3,

    /**
     * maxRoutes
     * ------------------------------------------------------------
     * Максимальное количество страниц для проверки.
     *
     * У тебя sitemap нашёл около 72 страниц, поэтому 500 — безопасный запас.
     * Если сайт вырастет, лимит не остановит аудит слишком рано.
     */
    maxRoutes: 500,

    /**
     * sitemap
     * ------------------------------------------------------------
     * true = использовать sitemap.xml.
     *
     * Для Hugo это лучший вариант, потому что Hugo генерирует sitemap.xml
     * со списком важных страниц.
     */
    sitemap: true,

    /**
     * robotsTxt
     * ------------------------------------------------------------
     * false = НЕ учитывать robots.txt при техническом аудите.
     *
     * Почему false:
     * У тебя Unlighthouse уже показал:
     * "Skipping route based on robots.txt rule /"
     *
     * Значит, если оставить robotsTxt: true, Unlighthouse может пропускать
     * все страницы и аудит будет бесполезным.
     *
     * Важно:
     * Это НЕ меняет твой реальный robots.txt на сайте.
     * Это только говорит Unlighthouse не блокировать техническую проверку.
     */
    robotsTxt: false,

    /**
     * crawler
     * ------------------------------------------------------------
     * true = Unlighthouse может дополнительно искать внутренние ссылки.
     *
     * Но если sitemap найден, Unlighthouse часто использует sitemap
     * как основной источник маршрутов.
     */
    crawler: true,

    /**
     * skipJavascript
     * ------------------------------------------------------------
     * true = не выполнять JavaScript для поиска ссылок.
     *
     * Для Hugo-сайта это нормально, потому что страницы статические:
     * ссылки уже есть в HTML.
     *
     * Плюсы:
     * - быстрее;
     * - стабильнее;
     * - меньше лишней нагрузки.
     *
     * Если когда-нибудь сайт станет SPA и ссылки будут появляться
     * только через JS, тогда можно поставить false.
     */
    skipJavascript: true,

    /**
     * ignoreI18nPages
     * ------------------------------------------------------------
     * false = НЕ игнорировать мультиязычные страницы.
     *
     * Для твоего сайта это важно, потому что есть:
     * - украинская версия;
     * - русская версия /ru/.
     *
     * Мы хотим проверять обе версии, потому что там могут отличаться:
     * - title;
     * - description;
     * - canonical;
     * - hreflang;
     * - schema.org;
     * - lang в html.
     */
    ignoreI18nPages: false,

    /**
     * dynamicSampling
     * ------------------------------------------------------------
     * false = меньше "умной группировки" похожих страниц.
     *
     * Для небольшого Hugo-сайта лучше проверять страницы подробнее,
     * а не слишком сильно группировать похожие маршруты.
     */
    dynamicSampling: false,

    /**
     * exclude
     * ------------------------------------------------------------
     * Список URL, которые НЕ нужно проверять.
     *
     * Здесь мы исключаем:
     * - технические страницы;
     * - мусорные bot-scan URL;
     * - системные файлы;
     * - служебные маршруты;
     * - то, что не является HTML-страницей для SEO-аудита.
     */
    exclude: [
      /**
       * Hugo taxonomy / pagination.
       * Если хочешь проверять tags/categories — убери эти строки.
       */
      '/tags/**',
      '/categories/**',
      '/page/**',

      /**
       * WordPress-боты часто сканируют эти пути.
       * У тебя Hugo-сайт, поэтому эти URL не нужны для аудита.
       */
      '/wp-**',
      '/wp-admin/**',
      '/wp-content/**',
      '/wp-includes/**',
      '/xmlrpc.php',

      /**
       * Служебные и потенциально опасные пути, которые часто ищут боты.
       */
      '/.git/**',
      '/.env',
      '/.env.**',
      '/openid_connect/**',
      '/cpanel/**',

      /**
       * Файлы, которые не являются HTML-страницами.
       */
      '/sitemap.xml',
      '/robots.txt',
      '/manifest.webmanifest',
      '/site.webmanifest',
      '/sw.js'
    ]
  },

  /**
   * lighthouseOptions
   * ------------------------------------------------------------
   * Эти настройки передаются напрямую в Google Lighthouse.
   */
  lighthouseOptions: {
    /**
     * onlyCategories
     * ------------------------------------------------------------
     * Какие категории проверять.
     *
     * Для SEO/Performance-аудита Hugo-сайта главные:
     * - performance;
     * - accessibility;
     * - best-practices;
     * - seo.
     *
     * PWA можно проверять отдельно обычным Lighthouse,
     * потому что Unlighthouse чаще используют для массового аудита страниц.
     */
    onlyCategories: qualityCategories,

    /**
     * clearStorage
     * ------------------------------------------------------------
     * true = очищать cookies/localStorage/sessionStorage перед аудитом.
     *
     * Это делает проверку чище и ближе к первому визиту пользователя.
     */
  },

  /**
   * ci
   * ------------------------------------------------------------
   * Настройки для автоматической проверки качества.
   *
   * Даже если ты пока не используешь CI/CD для Unlighthouse,
   * эти бюджеты полезно держать в конфиге как стандарт качества.
   */
  ci: {
    /**
     * budget
     * ------------------------------------------------------------
     * Минимальные допустимые оценки.
     *
     * Для твоей цели:
     * - Performance желательно 95–100;
     * - Accessibility 100;
     * - Best Practices 100;
     * - SEO 100.
     *
     * Performance не ставим 100 как обязательный минимум,
     * потому что Lighthouse может немного прыгать между запусками.
     */
    budget: {
      performance: 95,
      accessibility: 100,
      'best-practices': 100,
      seo: 100
    },

    /**
     * buildStatic
     * ------------------------------------------------------------
     * true = разрешить генерацию статического отчёта.
     */
    buildStatic: true
  }
})
